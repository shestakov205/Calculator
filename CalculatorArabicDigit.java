public class CalculatorArabicDigit {

    public int add(int x, int y) {
        int result;
        result = x + y;
        return result;
    }

    public int minus(int x, int y) {
        int result;
        result = x - y;
        return result;
    }

    public int multiplication(int x, int y) {
        int result;
        result = x * y;
        return result;
    }

    public int division(int x, int y) {
        int result;
        result = x / y;
        return result;
    }
}
